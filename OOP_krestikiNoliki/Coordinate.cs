
namespace OOP_krestikiNoliki
{
    public class Coordinate
    {
        private int coorX;
        private int coorY;

        public Coordinate(int x, int y)
        {
            coorX = x;
            coorY = y;
        }

        public int X()
        {
            return coorX;
        }

        public int Y()
        {
            return coorY;
        }

    }
}
